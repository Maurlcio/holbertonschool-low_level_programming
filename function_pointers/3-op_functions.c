/**
 * op_add - baby math for smart babies
 * @a: a
 * @b: b
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - more math
 * @a: a
 * @b: b
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

